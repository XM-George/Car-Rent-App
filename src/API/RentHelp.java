package API;


/**
 * Η κλάση {@code RentHelp} παρέχει βοηθητικές λειτουργίες
 * που σχετίζονται με τη διαχείριση ενοικιάσεων.
 * <p>
 * Συγκεκριμένα, περιλαμβάνει μεθόδους που αφορούν
 * την ολοκλήρωση μιας ενοικίασης και την επιστροφή
 * ενός οχήματος από τον πελάτη.
 */
public class RentHelp
{

    /**
     * Δημιουργεί ένα νέο αντικείμενο {@code RentHelp}.
     */
    public RentHelp()
    {
        // default constructor
    }

    /**
     * Ολοκληρώνει την επιστροφή ενός οχήματος που έχει ενοικιαστεί.
     * <p>
     * Η μέθοδος:
     * <ul>
     *   <li>Αφαιρεί την ενοικίαση από τις ενεργές ενοικιάσεις του πελάτη</li>
     *   <li>Αποδεσμεύει το όχημα από την ενεργή ενοικίαση</li>
     *   <li>Αλλάζει την κατάσταση του οχήματος σε διαθέσιμο</li>
     *   <li>Ενημερώνει την κατάσταση της ενοικίασης ως ολοκληρωμένη</li>
     * </ul>
     *
     * @param r η ενοικίαση που ολοκληρώνεται με την επιστροφή του οχήματος
     */
    public void returnVehicle(Rent r)
    {
        r.getCustomer().getActiveRents().remove(r);
        r.getVehicle().setActiveRent(null);
        r.getVehicle().setState("Διαθέσιμο");
        r.setState("Το Όχημα Επιστράφηκε");
    }

}
